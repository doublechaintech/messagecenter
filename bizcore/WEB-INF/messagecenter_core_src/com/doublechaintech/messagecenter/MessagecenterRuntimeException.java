
package com.doublechaintech.messagecenter;


public class MessagecenterRuntimeException extends RuntimeException {
    static final long serialVersionUID = -1;

    public MessagecenterRuntimeException() {
        super();
    }


    public MessagecenterRuntimeException(String message) {
        super(message);
    }


    public MessagecenterRuntimeException(String message, Throwable cause) {
        super(message, cause);
    }


    public MessagecenterRuntimeException(Throwable cause) {
        super(cause);
    }


   
}









